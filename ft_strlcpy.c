#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t		i;
	const char	*temp;
	size_t		len;

	i = 0;
	len = 0;
	temp = src;
	while (*temp++)
	{
		len++;
	}
	if (size == 0)
	{
		return (len);
	}
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (len);
}
